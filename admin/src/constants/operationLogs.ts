function humanizeDottedKey(key: string): string {
  const normalized = key.replace(/_/g, '.');
  const parts = normalized.split('.').filter(Boolean);
  if (parts.length === 0) return key;
  const translated = parts.map((part) => {
    if (SEGMENT_LABEL[part]) return SEGMENT_LABEL[part];
    const chunks = part.split('_').filter(Boolean);
    if (chunks.length <= 1) return part;
    return chunks.map((c) => SEGMENT_LABEL[c] || c).join('');
  });
  return translated.join(' · ');
}

export function operationLogResourceLabel(resource?: string): string {
  const k = (resource || '').trim();
  if (!k) return '—';
  return OPERATION_LOG_RESOURCE_LABEL[k] || humanizeDottedKey(k);
}

export function operationLogActionLabel(action?: string): string {
  const k = (action || '').trim();
  if (!k) return '—';
  return OPERATION_LOG_ACTION_LABEL[k] || humanizeDottedKey(k);
}

